# tipe data Boolean 
print(True)
print(False)

# tipe data String
print("Ayo belajar Python")
print('Belajar Python sangat Mudah')

# tipe data Integer 
print(20)

# tipe data Float 
print(3.14)

# tipe data Hexadecimal
#print(9a)

# tipe data Complex 
print(5j)

# tipe data List 
print([1,2,3,4,5])
print(["satu", "dua", "tiga"])

# tipe data Tuple 
print((1,2,3,4,5))
print(("satu", "dua", "tiga"))

# tipe data Dictionary 
print({"nama":"Budi",'umur':20})

# tipe data Dictionary dimasukkan ke dalam variabel biodata 
biodata = {"nama":"Andi",'umur':21} # proses instalasi variabel biodata 
print(biodata) # proses pencatakan variabel biodata berisi tipe data Dictionary 
print(type(biodata)) # fungsi untuk mengecek jenis tipe data akan tampil <class 'dict'> yang berarti dict adalah tipe data dictionary


